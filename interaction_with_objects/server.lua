function openRandomPackCards(event, item, inventory, slot, data)
    local playerId = inventory.id
    local User = API.GetUserFromSource(playerId)

    if event == 'usingItem' then
        local amountPerPack = 5

        for i = 1, amountPerPack do 
            local randomItems = math.random(1, #cardsItems)
            local itemFromList = cardsItems[randomItems]

            local itemLabel = libClient.getLabelTextByHash( playerId, itemFromList )

            local itemMetadata = {
                label = itemLabel,
                image = itemFromList,
                model = itemFromList,
                prop = itemFromList,
            }

            exports.ox_inventory:AddItem(playerId, "collectable_card", 1, itemMetadata)
        end
        return true
    end

    if event == 'usedItem' then
        exports.ox_inventory:RemoveItem(playerId, item.name, 1)
        return true
    end

    if event == 'buying' then
        return true
    end
    
    return false
end

exports('openRandomPackCards', openRandomPackCards)


RegisterNetEvent("frp:interaction:fillCanteen", function()
    local playerId = source

    local itemSlot = exports.ox_inventory:GetSlotWithItem(playerId, "canteen_full")

    if itemSlot then
        exports.ox_inventory:SetDurability(playerId, itemSlot.slot, 100)
    end

end)

RegisterNetEvent("frp:interaction:fillBucket", function()
    local playerId = source
    local itemName =  "empty_watering_can"

    local Item = exports.ox_inventory:GetItem(playerId, itemName, nil, true)

    if itemName == "empty_watering_can" then
        local itemReward = "watering_can"

        if Item >= 1 and itemReward then
            exports.ox_inventory:RemoveItem(playerId, itemName, Item)
            exports.ox_inventory:AddItem(playerId, itemReward, Item)
        else
            cAPI.Notify(playerId, "error", "Você não possuí 1 Balde Vazio", 3000)
        end

        return
    end
end)